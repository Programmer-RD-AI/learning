public class If{
    public static void main(String[] args){
        int age = 18;
        if (age >= 75){
            System.out.println("OK boomer!");
        }
        else if (age >= 18){
            System.out.println("You are an adult");
        }
        else{
            System.out.println("You are an child");
        }
        return;
    }
}